-- Create Database
CREATE DATABASE MandiriUtamaFinanceTest;
USE MandiriUtamaFinanceTest;

-- Create Job table
CREATE TABLE Job (
    EMPL_JOB_ID VARCHAR(10) PRIMARY KEY,
    EMPL_JOB_DESC VARCHAR(100)
);


-- Create EMPL table
CREATE TABLE EMPL (
    EMPL_NAME VARCHAR(50),
    EMPL_JOB_ID VARCHAR(10),
    EMPL_SALARY INT,
    FOREIGN KEY (EMPL_JOB_ID) REFERENCES Job(EMPL_JOB_ID)
);

-- Create MASTER_CABANG table
CREATE TABLE MASTER_CABANG (
    KODE_CABANG CHAR(4) PRIMARY KEY,
    NAMA_CABANG VARCHAR(50),
    TIPE_CABANG VARCHAR(10),
    KODE_AREA CHAR(4)
);

-- Create MASTER_CUST table
CREATE TABLE MASTER_CUST (
    KODE_CABANG CHAR(4),
    KODE_NASABAH CHAR(15) PRIMARY KEY,
    NAMA_NASABAH VARCHAR(50),
    FOREIGN KEY (KODE_CABANG) REFERENCES MASTER_CABANG(KODE_CABANG)
);

-- Create TRANS_CUST table
CREATE TABLE TRANS_CUST (
    KODE_CABANG CHAR(4),
    NOMOR_KONTRAK CHAR(15) PRIMARY KEY,
    KODE_NASABAH CHAR(15),
    HARGA_UNIT MONEY,
    TANGGAL_TRANSAKSI DATETIME,
    FOREIGN KEY (KODE_CABANG) REFERENCES MASTER_CABANG(KODE_CABANG),
    FOREIGN KEY (KODE_NASABAH) REFERENCES MASTER_CUST(KODE_NASABAH)
);

